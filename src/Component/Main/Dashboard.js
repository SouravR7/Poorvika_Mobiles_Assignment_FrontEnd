function Dashboard() {
  const history = useHistory();
  const classes = useStyles();
  const [open, setOpen] = useState(false);
  const [admin, setAdmin] = useState(false);
  const [username, setUsername] = useState("");
  useEffect(() => {
    if (!window.localStorage.getItem("login")) {
      //console.log("Click");
      history.push("/");
      return;
    }
    const login = JSON.parse(window.localStorage.getItem("login"));
    console.log(login.isAdmin);
    setUsername(login.name);
    if (login.isAdmin) {
      setAdmin(login.isAdmin);
      history.push("/dashboard/Admin");
    } else {
      history.push("/dashboard/User");
    }
    //console.log(login.name);
    //setUsername(login.name);
  }, [admin]);

  const handleDrawerOpen = () => {
    setOpen(true);
  };
  const handleDrawerClose = () => {
    setOpen(false);
  };
  return (
    <div className={classes.root}>
      <CssBaseline />
      <AppBar
        position="absolute"
        className={clsx(classes.appBar, open && classes.appBarShift)}
      >
        <Toolbar className={classes.toolbar}>
          <IconButton
            edge="start"
            color="inherit"
            aria-label="open drawer"
            onClick={handleDrawerOpen}
            className={clsx(
              classes.menuButton,
              open && classes.menuButtonHidden
            )}
          >
            <MenuIcon />
          </IconButton>
          <Typography
            component="h1"
            variant="h6"
            color="inherit"
            noWrap
            className={classes.title}
          >
            Sports Blog
          </Typography>

          <Typography
            component="h1"
            variant="h6"
            color="inherit"
            noWrap
            className={classes.title}
            display="flex"
            flexDirection="row"
            justifyContent="center"
            alignItems="center"
          >
            <IconButton color="inherit">
              <Badge color="secondary">
                <AccountCircleIcon />
              </Badge>
            </IconButton>
            {username}
          </Typography>
        </Toolbar>
      </AppBar>
      <Drawer
        variant="permanent"
        classes={{
          paper: clsx(classes.drawerPaper, !open && classes.drawerPaperClose),
        }}
        open={open}
      >
        <div className={classes.toolbarIcon}>
          <IconButton onClick={handleDrawerClose}>
            <ChevronLeftIcon />
          </IconButton>
        </div>
        <Divider />
        <List>
          <ListItems />
        </List>
        <Divider />
      </Drawer>
      <main className={classes.content}>
        <div className={classes.appBarSpacer} />
        <Container maxWidth="lg" className={classes.container}>
          {admin ? (
            <Route exact path="/dashboard/Admin" component={Admin} />
          ) : (
            <Route exact path="/dashboard/User" component={User} />
          )}
        </Container>
        <Switch>
          <Route exact path="/dashboard/Admin/:id" component={DetailPost} />
          <Route exact path="/dashboard/User/:id" component={DetailPost} />
        </Switch>
      </main>
    </div>
  );
}

export default Dashboard;
